package exercicios;

public class ExerciciosAula {
	public static void main(String[]args) {
		
		/*
		Exemplo 01: Incrementação e Decrementação de valores utilizando ++ e --
		int x = 10;
		System.out.println(x++); // Valor ++, mostra depois adiciona 
		System.out.println(++x); // ++ Valor, adiciona para depois mostrar
		System.out.println(x--); // Valor --. mostra depois subtrai
		System.out.println(--x); // -- Valor, subtrai depois mostra
		*/
		
		/* Exemplo 02: Neste caso ele irá incrementar o valor e somar mais uma incrementação do valor
		int a = 10;
		int b = 10;
		System.out.println(a++ + ++a);//O resultado é 22
		System.out.println(b++ + b++);//O resultado é 21
		*/
		
		/* 
		Exemplo 03: O simbolo ~ inverte e subtrai 1 do valor e ! mostra o valor oposto
		int a = 10;
		int b = -10;
		
		boolean c = true;
		boolean d = false;
		
		System.out.println(~a); //O resultado é -11 (o valor total positivo começa em 0)
		System.out.println(~b); //O resultado é 9 (O positivo total é menor pois o positivo começa em 0)
		System.out.println(!c); //O resultado é mostrado como False
		System.out.println(!d); //O resultado é mostrado como true
		*/
		
		/* 
		 Exemplo 04: Os operadores aritmédicos em Java são utilizados para realizar adição, subtração, multiplicação e divisão. 
		 Eles atuam como operadores de matemáticas básicas.
		 
		int a = 10;
		int b = 5;
		
		System.out.println(a+b);//15
		System.out.println(a-b);//5
		System.out.println(a*b);//50
		System.out.println(a/b);//2
		System.out.println(a%b);//0
		*/
	}
}
